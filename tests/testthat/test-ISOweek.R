test_that("ISOweek checks input", {
  expect_error(ISOweek("not a date"), "`date` must be a date or datetime")
})

test_that("ISOweek returns correct dates", {
  dates <- ymd(
    "1970-07-24", "1971-07-19", "1972-05-30", "1971-03-12", "1974-01-31",
    "1973-08-13", "1974-03-28", "1972-04-16", "1970-12-30", "1970-04-20",
    "1971-05-28", "1972-02-08", "1970-01-15", "1971-12-05", "1971-04-03",
    "1970-06-16", "1975-03-23", "1970-04-28", "1971-05-16", "1970-09-09",
    "1970-08-19", "1973-05-16", "1973-11-30", "1974-11-29", "1973-07-19",
    "1971-05-26", "1971-10-30", "1972-02-08", "1972-02-15", "1974-12-12"
  )
  isoweeks <- c(
    "1970-W30", "1971-W29", "1972-W22", "1971-W10", "1974-W05", "1973-W33",
    "1974-W13", "1972-W15", "1970-W53", "1970-W17", "1971-W21", "1972-W06",
    "1970-W03", "1971-W48", "1971-W13", "1970-W25", "1975-W12", "1970-W18",
    "1971-W19", "1970-W37", "1970-W34", "1973-W20", "1973-W48", "1974-W48",
    "1973-W29", "1971-W21", "1971-W43", "1972-W06", "1972-W07", "1974-W50"
  )
  expect_identical(ISOweek(dates[[1]]), isoweeks[[1]])
  expect_identical(ISOweek(dates), isoweeks)
})
